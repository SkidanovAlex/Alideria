<p>Чтобы улучшить заклинание, вам придётся набрать определённое количество очков в игре "Марблс" (она же "Линии" или "Шарики", те самые, в которые давным-давно играли многие).
 
Не ругайтесь. Это сделано специально для того, чтобы игроки не смогли заполучить себе сумасшедшие силы сразу же.

