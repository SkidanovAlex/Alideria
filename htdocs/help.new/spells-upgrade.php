<p>
 
 Правила самой игры в шарики просты.
 Нужно выстраивать линии из четырёх шариков или больше, перемещая уже появившиеся. Если клетки "заперты" другими шарами, переместить не удастся.
 Линии можно выстраивать по вертикали, по горизонтали и по диагонали. 
 <font color="darkblue"><b>Чем больше шаров в созданном ряду, тем больше очков вы получаете!</b></font>
</p>
