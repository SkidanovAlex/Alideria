<p> 
 Если вы набрали столько очков, сколько нужно, можете выходить из окна: посох заряжен!<br />
 Его можно продать, обменять или положить на склад.<br /><br />
 Чтобы применить улучшение, нужно проверить, есть ли у вас нужное заклинание, взять посох в руки (Инвентарь - щёлкнуть на название посоха - Использовать) и улучшить нужное заклинание!
</p>
 
 

