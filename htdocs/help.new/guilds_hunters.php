2. Охота на зайцев. На опушке леса вы можете охотиться на зайцев (зайцев вы можете поймать и 
в охотничьей базе, это лишь альтернативный вариант). Для этого необходимо найти зайца на опушке,
что в среднем случае должно занять мало времени, после чего нажать "Ловить зайца". По истечение 30-ти секунд
вы либо поймаете зайца, либо он убежит в одну из 8 сторон, и поиск придется продолжить там. Если в течение двух минут
этого не сделать, зайца придется искать заново. Следует быть готовым к тому, что
заяц может очень долго от вас убегать.<br>
3. Капканы. Достигнув рагна 1 вы можете ставить капканы. Капканы
ставятся на 24 часа, в течение которых вы можете заниматься любыми делами.
Через 24 часа вам следует проверить капканы на предмет добычи. Чем позже вы проверите
капканы, тем меньше шанс найти там хорошую добычу.<br>
