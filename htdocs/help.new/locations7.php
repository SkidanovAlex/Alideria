<p>
При нажатии на кнопку &quot;Исследовать здесь&quot; вы начнёте поиск. В результате поиска вы можете ничего <b><font color="blue">не найти</font></b>, найти <b><font color="blue">что-то полезное</font></b> и найти <b><font color="blue">монстра</font> на свою голову</b>. Помните, что вы можете что-то найти <b>и во время перехода</b> между глубинами!<br />
