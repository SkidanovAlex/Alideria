<p>
В каждый момент времени в пещере вы однозначно будете видеть кнопки &quot;<b>Вернуться
Назад</b>&quot; (подняться на верхние уровни Пещер или выйти наружу) и &quot;<b>Исследовать Здесь</b>&quot; (попробовать отыскать что-нибудь полезное). Если вы можете спуститься глубже, то вы также будете видеть кнопку &quot;<b>Двигаться Глубже</b>&quot;, которая переместит вас на глубину ниже. 
</p>
