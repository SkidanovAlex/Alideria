<p>Пещера разбита на глубины сверху вниз: от самой лёгкой нулевой до мрачных, сложных и опасных каменных коридоров.<br>
<b><font color="blue">Первые три глубины</font></b> (<b>нулевая, первая и вторая</b>) находятся достаточно близко к входу и в них достаточно светло, чтобы можно было искать различные вещи и драться с местными монстрами, не опасаясь встретить серьёзных противников.<br />
Глубины 3 и глубже уже скрыты во тьме, и для перемещения в кромешной тьме вам не помешает раздобыть пару-тройку факелов...
</p>

