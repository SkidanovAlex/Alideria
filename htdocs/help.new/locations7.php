<p>
<b><font color="blue">При первом посещении</font> любой глубины</b>, кроме нулевой, вы будете гарантированно <b>атакованы <font color="blue">Стражем Глубины</font></b> - монстром, который ее охряняет. До тех пор, пока вы не победите его, он будет продолжать вас атаковать при попытке пройти.<br />
После первой победы глубина будет считаться исследованной вами, и монстры будут нападать на вас со стандартными шансами. Обычно монстры атакуют с шансом в <b><font color="blue">33%</font></b>.
</p>

<p>
На некоторых глубинах пещеры вы можете найти другие локации. Так, на 1-ой глубине вы можете спуститься в <a href=help.php?id=50029>Лабиринт Кошмаров</a>, а на 0-ой и 3-ей глубине члены гильдии старателей могут найти Прииск Семи Старателей и Зачарованную Шахту.
</p>

		
