Среди множества преимуществ Орденов стоит отметить <b>возможность <font color="blue">строить здания</font></b>, которые могут пригодиться всем членам Ордена.<br>
Одни дают Ордену и его членам дополнительные преимущества, другие при умелом использовании могут обеспечить ему эффективное развитие, третьи&nbsp;же помогают быстрее строить другие здания.<br><br>

