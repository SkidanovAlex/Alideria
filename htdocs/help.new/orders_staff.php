Для каждого звания или должности можно выставить свой набор привилегий. При выставлении <b>доступа к <font color="blue">складу Ордена</font></b> можно указать <b><font color="blue">цвета полок</font></b>, которые будут доступны этому рангу послушников или магистров.<br>
Магистр с возможностью <b>менять права других членов Ордена</b> может выставлять другим магистрам или послушникам только те права, которые доступны ему самому.
</p>

