<h2>Звания и должности</h2>
<p>
У каждого члена Ордена имеются <b><font color="blue">звание и должность</font></b>, которое зависит от его ранга в Ордене, от его возможностей и обязанностей. В своём роде звание и должность - это ключи к тайным дверям Орденских комнат.<br>
При создании Ордена <b>игрок, подавший заявку</b>, автоматически становится <b><font color="blue">Магистром</font> Ордена и <font color="green"><u>единственным</u> Главой</font></b>, получающим в руки все &quot;ключи&quot; к тайным дверям. <b><font color="blue">Остальные</font> члены Ордена</b> автоматически <b>становятся его <font color="blue">Послушниками</font></b>; не рекомендуется оставлять их в этом же состоянии.<br>
Впоследствии можно создать дополнительные звания и должности для каждого члена Ордена, поставив в связь с каждой группой послушников и магистров определённые возможности и обязанности.
</p>
