<h3>Настройка Званий и Должностей</h3>
<p>
В Зале Собраний можно настроить <b><font color="blue">привилегии</font>, связанные <font color="blue">с тем или иным званием или должностью</font></b>. К примеру, можно определить, кто из членов Ордена имеет право доступа к Складу и Казначейству, восстанавливать здоровье в Столовой, следить за форумом и мини-сайтом Ордена или управлять строительством зданий и приёмом послушников.<br>
Каждому званию или должности соответствует <b>определённый <font color="blue">набор параметров</font></b>, выставленных Главой или магистрами Ордена. <b>Выставить определённый набор привилегий <font color="red">определённому игроку нельзя</font>.</b>
</p>
<div class="hint"><table><tr><td valign=top><img src="help/etc/help-twinkle.gif" /></td><td valign="top" class="tiny">
<img src="help/etc/help-order-staff-menu.png">
<p>
У каждого звания или должности имеется <b>личный номер от <font color="blue">0</font> до <font color="blue">1000</font></b>, который выбирается при создании. Указав ID уже созданного звания или должности, можно <b><font color="blue">переименовать</font></b> её; при этом выставленные привилегии останутся теми же самыми.<br>
<b><u>Должность Главы</u> <u><font color="red">нельзя переименовать, изменить или удалить</font></u></b>. Её может держать при себе <b>только <font color="blue">один игрок</font></b>. Хотя никто не запрещает создавать видимость &quot;многоглавия&quot;.</p>
