Баланс изменяется, когда игрок кладёт или забирает со склада ресурсы или вещи, а также при добавлении ресурсов в казну. С его помощью можно защитить Орден от любителей жить роскошно за счёт других жителей Алидерии.<br><br>
Галочки справа <b><u>только</u> указывают</b> на уже имеющиеся привилегии персонажа (зависят от звания и должности).<br>
