<p style="font-size: 110%; line-height: 120%;">
<b>Совет:</b> <b><span style="color:#AA0000;">НЕ</span></b> открывайте <b>полный</b> доступ к складу всем членам Ордена. Всем, в особенности послушникам, выставьте неотрицательный баланс (ниже описано, как это сделать) и разрешите доступ к вещам Ордена ТОЛЬКО при неотрицательном балансе.<br />
Особенно хорошо <b>защитите <span style="color:#AA0000;">красную</span> полку склада,</b> предназначенную для еды и строительства зданий. У всех игроков для этой полки должен быть доступ &quot;ТОЛЬКО КЛАСТЬ&quot;. Почти наверняка ни одному игроку не понадобится брать что-нибудь с красной полки.<br /><br />
Если в Ордене стоят права на склад &quot;Можно брать&quot; и человек заберет все вещи со склада, Администрация не будет рассматривать этот случай и не будет возвращать утерянные вещи, поскольку это, вне всяких сомнений, ошибка, допущенная руководством самого Ордена.
</p></td></tr></table></div>
