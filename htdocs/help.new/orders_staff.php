<h3>Выставление Привилегий и Очки Действия</h3>
<p>
Для того, чтобы тот или иной послушник или магистр получил те или иные права или обязанности, их нужно выставить в <nobr><b>меню <font color="blue">управления составом</font> Ордена</b>.</nobr><br>
Права и возможности игрока устанавливаются привилегиями, выставленными для <b>его <font color="green">звания и должности</font></b>. Выставить привилегии <nobr>лично игроку нельзя!</nobr><br>
Для некоторых важных действий по управлению кланом требуется <b>наличие <font color="blue">Очков Управления</font></b> - это сделано специально для <nobr><b>защиты от &quot;внутренних беспорядков&quot;</b>.</nobr><br>
Почти все действия, связанные с контролем и изменением устройства Ордена, тратят Очки Управления. <b><font color="blue">Пополнить</font></b> их запас другим игрокам могут магистры с <b><font color="blue">неограниченным</font> количеством Очков Управления</b>. <b><font color="red">Сами по себе Очки Управления не восполняются!</font></b>
</p>
