<p>
В реке, как ни странно, плавают не только рыбы, водные духи и монстры. Любой маг, познав мощь Воды, может находиться под водой достаточно долго - главное не открывать любимую флягу на дне...<br />
Местные существа обычно равнодушно относятся к людям. Рыбам нет до них дела, тритоны и мурены стараются избежать встреч с особо ретивыми чародеями, а глубинные монстры просто дремлют в ожидании.
</p>

