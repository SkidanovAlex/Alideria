<h3>Добыча в гильдиях</h3>
<p>В мире Алидерии живут и работают представители множества различных профессий. Многие из этих людей объединились в гильдии, к которым можете присоединиться и Вы.<br>
Вы сможете обеспечить себе неплохой заработок, вступив в одну или несколько добывающих гильдий  - скажем, в гильдию <a href="help.php?id=34276">Рыбаков</a>, <a href="help.php?id=34278">Старателей</a>, <a href="help.php?id=34315">Охотников</a> или <a href="help.php?id=34277">Собирателей</a>.</p>

<p>В первых трёх добывающих гильдиях (<b><a href="help.php?id=34276">Рыбаки</a>, <a href="help.php?id=34278">Старатели</a>, <a href="help.php?id=34315">Охотники</a></b>) процесс добычи очень прост: Вы приходите в локацию добычи (прииск, охотничья база или рыбацкий домик) и запускаете ходку, после чего ждёте её завершения.<br>
Если ходка окажется успешной, в ваш инвентарь добавится некоторое количество ресурсов, зависящее от вашего положения в гильдии. Их впоследствии можно будет продать или использовать для создания каких-либо вещей или изделий.<br />
<b>Каждая такая ходка длится <b><font color="blue">10</font> минут</b>.</b><br>
Во время ходки Вы можете спокойно отвлечься на свои дела, к примеру, заняться работой или учёбой. Таким образом, заработок в этих гильдиях достаётся достаточно легко, ведь <b>для работы в этих гильдиях отвлекаться на игру нужно всего лишь <font color="blue">раз в 10 минут</font></b>.</p>

<p>В гильдиях Охотников и Рыбаков, помимо этого, при достижении определённого ранга можно <b><font color="blue">ставить сети</font> (в реке) <font color="blue">или капканы</font> (в лесу)</b>. Это особый вариант ходки, который ставится <b>на целые сутки</b>.<br>
После установки капканов или сетей Вы можете заниматься своими делами - даже зарабатывать в гильдии привычным способом! Но <b>не забудьте через 24 часа проверить ловушки</b>: чем дольше Вы будете тянуть с проверкой, тем меньше добычи Вы получите!<br>
<b>Примечание:</b> можно ставить <b>или капканы, или сети</b>, но не и то, и то.</p>
<br>
<p><b>О Гильдии <a href="help.php?id=34277">Собирателей</a></b> стоит поговорить отдельно.<br>
<b>Прибыль от работы в этой гильдии гораздо выше</b> по сравнению с любой другой добывающей гильдей. Однако для успешного собирательства требуется следить за происходящим вокруг и <b>всё время отвлекаться на игру</b>.<br>
Подробнее о работе в гильдии Собирателей Вы сможете прочесть <a href="help.php?id=34277">на странице самой гильдии</a>.<br>
Вкратце: Гильдия Собирателей <b>подходит тем, у кого есть много свободного времени</b>. В ней <b><font color="blue">можно заработать гораздо больше</font></b>, но <b>работа в ней <font color="blue">требует постоянного внимания</font> со стороны игрока</b>.
</p>
