<h3>Пещеры</h3>
<p>Пещеры - мрачное и таинственное место, с которого многие молодые маги начинают свой путь по Алидерии. В темноте подземных коридоров можно найти множество полезных вещей, которые можно продать другим игрокам и выручить несколько дублонов.<br>
Даже у входа в Пещеры (<b>на нулевом уровне</b>) есть возможность найти что-то ценное - например, там можно <b><font color="blue">заняться поиском камней</font></b>, которые впоследствии можно будет продать другим игрокам.<br>
Говорят, что на пятой и на десятой глубинах Пещер можно найти драгоценные камни, но искать их нужно гораздо дольше...</p>

