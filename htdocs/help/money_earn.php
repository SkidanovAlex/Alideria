<p><i>Каждому в мире Алидерии знаком дублон - мелкая золотая монета, еле заметно светящаяся изнутри в темноте. Дублоны издавна были в ходу у купцов Алидерии; постепенно эта монета стала официальной в столице и в других мелких городах и селеньях.<br>
За несколько дублонов Вы сможете пообедать или отдохнуть в ближайшей харчевне или таверне практически в любом уголке Алидерии. Кроме того, за это золото можно приобрести различные ценности и редкости, важные для ремесленника, странника, мудреца или мага.<br>
Дублоны в ходу везде. Дело за малым - направить их &quot;ход&quot; к Вам в карман.<br>
И здесь начинается самое сложное...</i></p>

