<h3>Западный Лес и Дозор</h3>
<p>В Западном Лесу располагается Дом Дозорных. Из него маги отправляются в дозоры в Западный Лес и на улицы города, наблюдая за порядком и безопасностью. Благодаря Дозорным жизнь простых горожан в столице течёт без страха и нервотрёпок.<br>
Вы можете <b>прийти в Дом Дозорных</b> и предложить свои услуги, <b>отправившись в дозор на несколько минут или даже часов</b>.<br>
За работу на Дом Дозорных Вы <b>можете получить деньги и, если повезёт, вещи</b>, которые могут пригодиться Вам в будущем.</p>

