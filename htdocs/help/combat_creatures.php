<p>
<b>Существа</b> - элемент боевой системы Алидерии, доступный игрокам с
4 уровня. Набравшийся опыта игрок может призвать на бой последователя, который будет атаковать противника или его существ, а также защищать своего хозяина от последователей врага.<br />
Игрок может призвать к себе на бой <b>до <font color="blue">3</font> существ</b>.
</p>

<p>
Для призыва существ имеются специальные заклинания. Некоторые из них можно найти в <b><font color="blue">Башне Тайных Знаний</font></b> среди прочих заклятий. Там можно найти заклинания призыва для <b><font color="blue">4</font> уровней</b> под каждую стихию.<br />
Все заклинания призыва <b><font color="red">не подвержены действию Удачи.</font></b>
</p>

