<h3>Специальные параметры существ</h3>
<p>
У каждого существа, помимо Атаки и Защиты, есть особенности, которые влияют на бой.
</p>

<p>
<b><font color="blue">Ярость</font></b> позволяет существу атаковать раньше, чем существо противника.<br />
Обычно существа наносят урон друг другу одновременно. Эта особенность даёт существу <b>нанести последователю врага урон раньше</b>, что даёт шанс расправиться с оппонентом без лишних ранений.
</p>

<p>
<b><font color="blue">Стремительность</font></b> позволяет существу <b>атаковать на том же ходу, на котором оно было призвано,</b> не оставляя первый удар оппонента без ответа.
</p>

<p>
<b><font color="blue">Подавление</font></b> позволяет после уничтожения последователя врага <b>нанести какой-то урон и самому врагу.</b> Чем меньше здоровья было у уничтоженного существа до удара, тем больший урон будет нанесён врагу.<br />
Урон расчитывается как <b>разница Атаки</b> существа с Подавлением <b>и оставшейся Защиты</b> существа-жертвы.
</p>

